#floor function - rounds down
#ceil function - rounds up

import math
num=12.3
num2=12.7
print("math.floor(num): ", math.floor(num))
print("math.floor(num2): ", math.floor(num2))
print("math.ceil(num): ", math.ceil(num))
print("math.ceil(num2): ", math.ceil(num2))

#round(x,y) - function to round numbers
#x - number to be rounded
#y - number of digits to be present after decimal point
print(round(34.58,1))
